function LogSwitch({ handleSwitche }) {
  const [isTableView, setIsTableView] = React.useState(true); // Initial view is table

  const handleChange = (event) => {
    setIsTableView(event.target.checked);
    handleSwitche(event.target.checked);
  };

  return (
    <FormGroup>
      <FormControlLabel
        control={
          <MaterialUISwitch
            sx={{ m: 1 }}
            checked={isTableView}
            onChange={handleChange}
          />
        }
        label={(isTableView)?"View as Chart":"View as Table"}
      />
    </FormGroup>
  );
}

